export default function Planner() {
  const mapRef = useRef<MapView>(null);

  const handlePoiClick = (event: any) => {
    const poi = event.nativeEvent;
    console.log("Clicked POI:", poi);
  };

  const moveTo = async (position: LatLng) => {
    const camera = await mapRef.current?.getCamera();
    if (camera) {
      camera.center = position;
      mapRef.current?.animateCamera({center: position, zoom: 10});
    }
  };

  const onPlaceSelected = (details: GooglePlaceDetail | null) => {
    const position = {
      latitude: details?.geometry.location.lat || 0,
      longitude: details?.geometry.location.lng || 0,
    };
    moveTo(position);
  };

  return (
    <View className="flex-1 pt-10 bg-gray-800">
      <View className="absolute flex-row w-9/12 self-center mt-9 rounded-md p-3 z-10">
        <GooglePlacesAutocomplete
          styles={{textInput: styles.input}}
          placeholder="Search"
          fetchDetails
          onPress={(data, details = null) => {
            onPlaceSelected(details);
          }}
          query={{
            key: GOOGLE_API_KEY,
            language: "en",
          }}
        />
      </View>
      <MapView
        className="w-full h-80"
        ref={mapRef}
        showsUserLocation={true}
        showsMyLocationButton={true}
        showsCompass={true}
        mapType="standard"
        customMapStyle={MapDarkStyle}
        onPoiClick={handlePoiClick}
      ></MapView>
    </View>
  );
}

const styles = StyleSheet.create({
  input: {
    width: "80%",
    zIndex: 10,
  },
});
